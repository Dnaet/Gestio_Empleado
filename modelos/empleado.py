class Empleado:
    def __init__(self, id, nombre, direccion, telefono, email, fecha_inicio, salario, tipo):
        self.id = id
        self.nombre = nombre
        self.direccion = direccion
        self.telefono = telefono
        self.email = email
        self.fecha_inicio = fecha_inicio
        self.salario = salario
        self.tipo = tipo
